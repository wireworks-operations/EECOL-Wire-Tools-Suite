/**
 * Get EECOL/Wesco products by cable type
 * @param {string} type - 'ACWU90', 'TECK90', 'RW90', 'NMD90', 'SOOW', 'FLEX', 'AC90', 'TELCO_FLEX', 'BARE'
 * @returns {Object} Products filtered by type
 */
